#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*p;

	i = 0;
	p = lst;
	if (!p)
		return (0);
	while (p)
	{
		p = p->next;
		i++;
	}
	return (i);
}
